    public void populatetheTable() {

        DefaultTableModel dtm = (DefaultTableModel) tableAbnormalVitalSigns.getModel();
        dtm.setRowCount(0);

        for (VitalSigns vs : vsh.getAbnormalList(maxBP, minBP)) {

            Object row[] = new Object[2];
            row[0] = vs;
            row[1] = vs.getBloodPressure();
            dtm.addRow(row);

        }

    }

