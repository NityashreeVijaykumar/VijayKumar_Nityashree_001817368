    private void btnViewDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDetailsActionPerformed
        // TODO add your handling code here:

        int selectedRow = tableAbnormalVitalSigns.getSelectedRow();

        if (selectedRow >= 0) {
            VitalSigns vs = (VitalSigns) tableAbnormalVitalSigns.getValueAt(selectedRow, 0);
            txtBloodPressure.setText(String.valueOf(vs.getBloodPressure()));
            txtTemperature.setText(String.valueOf(vs.getTemperature()));
            txtPulse.setText(String.valueOf(vs.getPulse()));
            txtDate.setText(vs.getDate());

        } else {
            JOptionPane.showMessageDialog(null, "No row is selected");
        }

    }//GEN-LAST:event_btnViewDetailsActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRow = tableAbnormalVitalSigns.getSelectedRow();

        if (selectedRow >= 0) {

            VitalSigns vs = (VitalSigns) tableAbnormalVitalSigns.getValueAt(selectedRow, 0);
            vsh.deleteVitals(vs);
            JOptionPane.showMessageDialog(null, "Record is Deleted");
            populatetheTable();

        } else {
            JOptionPane.showMessageDialog(null, "No row is selected");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

